#include <stdio.h>

int main() {
    char c = 'A'; // Declares a variable c of type char and assigns it the value 'A'.
    char v = 128; // Declares a variable v of type char and assigns it the value 128.
    printf("The value of the variable c is: %c\n", c); // Prints the value of the variable c to the console.
    printf("The value of the variable v is: %c\n", v); // Prints the value of the variable v to the console.
    return 0;
}
